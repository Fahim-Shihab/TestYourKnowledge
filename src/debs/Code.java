public class Code{
    String q, code, ans;

    public Code(String ans, String q, String code) {
        this.ans= ans;
        this.q = q;
        this.code = code;
    }

    public String getAns() {
        return ans;
    }

    public String getQ() {
        return q;
    }

    public String getCode() {
        return code;
    }
}